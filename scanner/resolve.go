// ResolveHostname tries multiple methods to resolve a hostname for the given IP:
// 1. Standard reverse DNS (PTR record)
// 2. mDNS reverse lookup (unicast query to host:5353)
func ResolveHostname(ip string) string {
	// Try standard reverse DNS with timeout
	ctx, cancel := context.WithTimeout(context.Background(), 1*time.Second)
	defer cancel()
	resolver := &net.Resolver{}
	names, err := resolver.LookupAddr(ctx, ip)
	if err == nil && len(names) > 0 {
		hostname := strings.TrimSuffix(names[0], ".")
		if hostname != "" {
			return hostname
		}
	}

	// Fallback: mDNS reverse lookup
	if name := mdnsReverseLookup(ip, 500*time.Millisecond); name != "" {
		return name
	}

	return "-"
}

// mdnsReverseLookup sends a unicast mDNS PTR query to the host on port 5353.
func mdnsReverseLookup(ip string, timeout time.Duration) string {
	parts := strings.Split(ip, ".")
	if len(parts) != 4 {
		return ""
	}
	// Build reverse name: 1.2.3.4 -> 4.3.2.1.in-addr.arpa
	name := fmt.Sprintf("%s.%s.%s.%s.in-addr.arpa", parts[3], parts[2], parts[1], parts[0])

	query := buildPTRQuery(name)

	conn, err := net.DialTimeout("udp", ip+":5353", timeout)
	if err != nil {
		return ""
	}
	defer conn.Close()

	conn.SetDeadline(time.Now().Add(timeout))
	if _, err := conn.Write(query); err != nil {
		return ""
	}

	buf := make([]byte, 1500)
	n, err := conn.Read(buf)
	if err != nil || n < 12 {
		return ""
	}

	return parsePTRResponse(buf[:n])
}

// buildPTRQuery builds a DNS PTR query packet for the given name.
func buildPTRQuery(name string) []byte {
	var buf []byte
	// Header
	buf = append(buf, 0x00, 0x00) // Transaction ID
	buf = append(buf, 0x00, 0x00) // Flags: standard query
	buf = append(buf, 0x00, 0x01) // Questions: 1
	buf = append(buf, 0x00, 0x00) // Answers: 0
	buf = append(buf, 0x00, 0x00) // Authority: 0
	buf = append(buf, 0x00, 0x00) // Additional: 0

	// Question name
	for _, label := range strings.Split(name, ".") {
		buf = append(buf, byte(len(label)))
		buf = append(buf, []byte(label)...)
	}
	buf = append(buf, 0x00)       // End of name
	buf = append(buf, 0x00, 0x0C) // Type: PTR (12)
	buf = append(buf, 0x00, 0x01) // Class: IN

	return buf
}

// parsePTRResponse extracts the hostname from a DNS response containing a PTR record.
func parsePTRResponse(data []byte) string {
	if len(data) < 12 {
		return ""
	}

	ancount := int(data[6])<<8 | int(data[7])
	if ancount == 0 {
		return ""
	}

	// Skip header
	offset := 12

	// Skip question section
	qdcount := int(data[4])<<8 | int(data[5])
	for i := 0; i < qdcount; i++ {
		offset = skipDNSName(data, offset)
		if offset < 0 || offset+4 > len(data) {
			return ""
		}
		offset += 4 // type + class
	}

	// Parse first answer
	offset = skipDNSName(data, offset)
	if offset < 0 || offset+10 > len(data) {
		return ""
	}

	rtype := int(data[offset])<<8 | int(data[offset+1])
	offset += 8 // type(2) + class(2) + TTL(4)
	if offset+2 > len(data) {
		return ""
	}
	offset += 2 // rdlength

	if rtype != 12 { // Not PTR
		return ""
	}

	name := readDNSName(data, offset)
	name = strings.TrimSuffix(name, ".")
	return name
}

// skipDNSName advances past a DNS name in wire format, returning the new offset.
func skipDNSName(data []byte, offset int) int {
	for offset < len(data) {
		length := int(data[offset])
		if length == 0 {
			return offset + 1
		}
		if length&0xC0 == 0xC0 { // Compression pointer
			return offset + 2
		}
		offset += 1 + length
	}
	return -1
}

// readDNSName reads a DNS name from wire format, following compression pointers.
func readDNSName(data []byte, offset int) string {
	var parts []string
	visited := make(map[int]bool)
	for offset < len(data) {
		if visited[offset] {
			break
		}
		visited[offset] = true

		length := int(data[offset])
		if length == 0 {
			break
		}
		if length&0xC0 == 0xC0 { // Compression pointer
			if offset+1 >= len(data) {
				break
			}
			offset = int(data[offset]&0x3F)<<8 | int(data[offset+1])
			continue
		}
		offset++
		if offset+length > len(data) {
			break
		}
		parts = append(parts, string(data[offset:offset+length]))
		offset += length
	}
	return strings.Join(parts, ".")
}

// GetARPTable parses the system ARP table and returns a map of IP -> MAC address.
func GetARPTable() map[string]string {
	table := make(map[string]string)

	var cmd *exec.Cmd
	switch runtime.GOOS {
	case "windows":
		cmd = exec.Command("arp", "-a")
	default:
		cmd = exec.Command("arp", "-an")
	}

	out, err := cmd.Output()
	if err != nil {
		return table
	}

	lines := strings.Split(string(out), "\n")
	for _, line := range lines {
		ip, mac := parseARPLine(line)
		if ip != "" && mac != "" {
			table[ip] = strings.ToUpper(mac)
		}
	}

	return table
}

// parseARPLine extracts IP and MAC from an ARP table line.
// Handles both macOS/Linux (`? (192.168.1.1) at aa:bb:cc:dd:ee:ff ...`)
// and Windows (`192.168.1.1    aa-bb-cc-dd-ee-ff    dynamic`) formats.
func parseARPLine(line string) (string, string) {
	line = strings.TrimSpace(line)
	if line == "" {
		return "", ""
	}

	// macOS/Linux format: ? (IP) at MAC on iface [ifscope]
	if strings.Contains(line, " at ") {
		parts := strings.Fields(line)
		if len(parts) < 4 {
			return "", ""
		}
		// Extract IP from parentheses
		ip := strings.Trim(parts[1], "()")
		mac := parts[3]
		if mac == "(incomplete)" || mac == "<incomplete>" {
			return "", ""
		}
		// Normalize MAC: ensure two-digit hex groups
		mac = normalizeMAC(mac)
		return ip, mac
	}

	// Windows format: IP  MAC  Type
	parts := strings.Fields(line)
	if len(parts) >= 2 {
		ip := parts[0]
		mac := parts[1]
		// Validate it looks like an IP
		if net.ParseIP(ip) == nil {
			return "", ""
		}
		// Windows uses dashes in MAC
		mac = strings.ReplaceAll(mac, "-", ":")
		mac = normalizeMAC(mac)
		return ip, mac
	}

	return "", ""
}

// normalizeMAC ensures each octet is two characters and uppercase.
func normalizeMAC(mac string) string {
	parts := strings.Split(mac, ":")
	if len(parts) != 6 {
		return mac
	}
	for i, p := range parts {
		if len(p) == 1 {
			parts[i] = "0" + p
		}
		parts[i] = strings.ToUpper(parts[i])
	}
	return strings.Join(parts, ":")
}

// LookupVendor returns the vendor name for the given MAC address.
// If the MAC is a locally administered (randomized) address, returns "Private".
func LookupVendor(mac string) string {
	if len(mac) < 8 {
		return "Unknown"
	}
	// Check locally administered bit (bit 1 of first octet).
	// Devices use randomized MACs for privacy; these have no OUI assignment.
	if isLocallyAdministered(mac) {
		return "Private"
	}
	prefix := strings.ToUpper(mac[:8])
	if vendor, ok := ouiTable[prefix]; ok {
		return vendor
	}
	return "Unknown"
}

// isLocallyAdministered returns true if the MAC has the locally administered
// bit set (bit 1 of the first octet), indicating a randomized/private address.
func isLocallyAdministered(mac string) bool {
	if len(mac) < 2 {
		return false
	}
	firstOctet, err := strconv.ParseUint(mac[:2], 16, 8)
	if err != nil {
		return false
	}
	return firstOctet&0x02 != 0
}
